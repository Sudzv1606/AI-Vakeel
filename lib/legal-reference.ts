/**
 * Get the section reference for a given legal domain.
 */
export function getSectionReference(domain: string): string {
  switch (domain) {
    case 'consumer_protection_2019':
      return CPA_2019_SECTIONS;
    case 'rera_2016':
      return RERA_2016_SECTIONS;
    case 'rti_2005':
      return RTI_2005_SECTIONS;
    default:
      return '';
  }
}
